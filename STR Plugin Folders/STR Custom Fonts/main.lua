m = gMarioStates[0]
screenWidth = djui_hud_get_screen_width()
screenHeight = djui_hud_get_screen_height()
width = 0 * 1
height = 16 * 1
x = (screenWidth - width) / 2.0
y = (screenHeight - height) / 2.0
end

